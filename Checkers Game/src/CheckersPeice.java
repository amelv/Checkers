
public class CheckersPeice {
	private String color;
	public CheckersPeice(String color)
	{

		this.color = color;
	}
	public CheckersPeice()
	{
		color = "#";
	}
	

	
	
	public String getColor()
	{
		return color;
	}
	
	public String toString()
	{
			return color;
	}
	
}
